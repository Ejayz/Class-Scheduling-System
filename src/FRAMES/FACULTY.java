    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Add_Faculty AF = new Add_Faculty();
        if (SECTION.getText().isEmpty() || DEPARTMENT.getText().isEmpty() || COURSE_TITLE.getText().isEmpty() || DAY.getSelectedItem().toString().equals("DAY")) {
            JOptionPane.showMessageDialog(this, "Error:Empty feilds are not allowed.");
        } else {
            int action = JOptionPane.showConfirmDialog(this, "Are you sure you want to add this schedule for faculty?", "Confirm Action", JOptionPane.OK_OPTION);
            if (action == 0) {
                AF.Splitter(SCHED.getSelectedItem().toString());
                if (AF.Add_Data(SECTION.getText(), DEPARTMENT.getText(), DAY.getSelectedItem().toString(), COURSE_TITLE.getText()) == 1) {
                    if (AF.UpdateIs_Used_Faculty(DAY.getSelectedItem().toString(), "true") == 1) {
                        JOptionPane.showMessageDialog(this, "Faculty schedule added succesfully");
                        AF.DisplayData(jTable2);
                        SECTION.setText("");
                        DEPARTMENT.setText("");
                        COURSE_TITLE.setText("");
                        DAY.setSelectedIndex(0);
                        SCHED.removeAllItems();

                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Error:Cant add schedult.Please contact support.");
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

