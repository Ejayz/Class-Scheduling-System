    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Add_Faculty AF = new Add_Faculty();
       Add_Room AR = new Add_Room();
        String Check = Start + "-" + End + "-" + Room;
        AF.Splitter(SCHED.getSelectedItem().toString());
        
        if (Days.equals(DAY.getSelectedItem()) && SCHED.getSelectedItem().toString().contains(Check)) {
            int result = JOptionPane.showConfirmDialog(this, "This action cannot be undone.Are you sure you want to delete this schedule?", "Confirm Action", JOptionPane.OK_OPTION);
            if (result == 0) {
                String pass = JOptionPane.showInputDialog("Enter your password to confirm this action.");
                if (AR.ValidateDelete(pass)) {
                   
                    System.out.println(SCHED.getSelectedItem().toString().equalsIgnoreCase(Check));
                    System.out.println(SCHED.getSelectedItem().toString());
                     System.out.println(Check);
                    if (AF.DeleteData(DAY.getSelectedItem().toString()) == 1) {
                        
                        
                        
                        if (AF.UpdateIs_Used_Faculty(DAY.getSelectedItem().toString(), "false") == 1) {
                           
                            SECTION.setText("");
                            DEPARTMENT.setText("");
                            COURSE_TITLE.setText("");
                            DAY.setSelectedIndex(0);
                            SCHED.removeAllItems();
                            jButton5.setVisible(false);
                            jButton2.setVisible(false);
                            jButton3.setVisible(true);
                            JOptionPane.showMessageDialog(this, "Schedule was deleted successfully");
                            AF.DisplayData(jTable2);
                        }
                        else{
                           JOptionPane.showMessageDialog(this, "Error:Unknown error.Please contact support.1");  
                        }
                    }
                    else{
                          JOptionPane.showMessageDialog(this, "Error:Unknown error.Please contact support.2");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Wrong password.Please try again.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Error:Schedule change cannot delete this data");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

