    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        Store_Selected st = new Store_Selected();
        Add_Teachers AT = new Add_Teachers();
        int row = jTable1.getSelectedRow();
        String name = String.valueOf(jTable1.getValueAt(row, 1));
        String ID = AT.getID(name);

        if (ID.equals("")) {
            JOptionPane.showMessageDialog(this, "An error occured:Cannot fetch data from data base to procced.");
        } else {

            FileCreator fc = new FileCreator();
            if (fc.DirChecker()) {
                fc.CreateDir();
            }
            if (fc.CreateFile("Storage.ini")) {
                System.out.print("result");
            }
            try{
FileWriter fw =new FileWriter("C://cs_system/Storage.ini");
fw.write(name+","+ID);
fw.flush();
fw.close();
            }
            catch(Exception e){
                System.out.println(e);
            }
            
            new ADDTEACHER().setVisible(true);
            this.dispose();
        }

    }//GEN-LAST:event_jTable1MouseClicked

