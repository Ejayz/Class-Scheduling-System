    private void ADDRECORDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ADDRECORDActionPerformed
        ID_GENERATOR GEN = new ID_GENERATOR();
        String Generated = GEN.GenerateId();
        Add_Teachers AT = new Add_Teachers();

        int result = AT.AddTeacher(Generated, FNAME.getText().toUpperCase(), MNAME.getText().toUpperCase(), LNAME.getText().toUpperCase(), DPARTMENT.getText().toUpperCase());

        if (result == 1) {
            JOptionPane.showMessageDialog(this, "Teachers data has beed added");
        AT.DisplayData(jTable1);
        } else {
            JOptionPane.showMessageDialog(this, "Cannot add data for now.if problem continue please contact support.");
        }


    }//GEN-LAST:event_ADDRECORDActionPerformed

